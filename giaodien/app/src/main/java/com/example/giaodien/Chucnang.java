package com.example.giaodien;

public class Chucnang {
    private int tainguyen;
    private String ten;

    public Chucnang(int tainguyen, String ten) {
        this.tainguyen = tainguyen;
        this.ten = ten;
    }

    public int getTainguyen() {
        return tainguyen;
    }

    public void setTainguyen(int tainguyen) {
        this.tainguyen = tainguyen;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
